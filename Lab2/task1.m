x = [11, 12, 13, 16, 17, 17, 18, 19, 22, 25];
y = [-5, -9, -14, -19, -22, -28, -35, -38, -44, -50];
n = 10;
plot(x,y);
grid on

a1 = (sum(x) * sum(y) - n * x * y') / (sum(x) ^ 2 - n * sum(x .^ 2));
a0 = 1 / n * (sum(y) - a1 * sum(x));

y1 = a0 + a1 * x;
hold on
plot(x, y1);
hold off
legend('Исходная функция', 'Аппроксимирующая функция');

